//increment -increment the value +1
let a=4 
console.log(a+1)//output will be 5 
console.log(a++)//output will be 4-post incremental 
console.log(a)//output will be 5 because once post incremental is done 
console.log(++a)//output will be 6 -pre incremental
console.log(a)

//decrement -decrease the value by -1
let c=33,d=66
//pre incremental
console.log(--c) //32
console.log(c)//32

//post incremental
console.log(d--) //66
console.log(d)//65

